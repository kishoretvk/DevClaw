                "def get_gpu_memory():\n",
                "    \"\"\"Get GPU memory in MB.\"\"\"\n",
                "    return torch.cuda.memory_allocated() / 1024 / 1024\n",
                "\n",
                "def run_baseline(model_name, prompt, max_tokens, num_runs=3):\n",
                "    \"\"\"Run baseline generation multiple times.\"\"\"\n",
                "    times = []\n",
                "    memories = []\n",
                "    texts = []\n",
                "    \n",
                "    for run in range(num_runs):\n",
                "        torch.cuda.empty_cache()\n",
                "        torch.cuda.synchronize()\n",
                "        \n",
                "        model = AutoModelForCausalLM.from_pretrained(\n",
                "            model_name,\n",
                "            torch_dtype=torch.float16,\n",
                "            device_map='auto'\n",
                "        )\n",
                "        tokenizer = AutoTokenizer.from_pretrained(model_name)\n",
                "        tokenizer.pad_token = tokenizer.eos_token\n",
                "        \n",
                "        input_ids = tokenizer(prompt, return_tensors='pt').to(model.device)\n",
                "        \n",
                "        mem_before = get_gpu_memory()\n",
                "        start = time.time()\n",
                "        \n",
                "        with torch.no_grad():\n",
                "            output = model.generate(\n",
                "                input_ids.input_ids,\n",
                "                max_new_tokens=max_tokens,\n",
                "                do_sample=True,\n",
                "                temperature=0.7,\n",
                "                pad_token_id=tokenizer.eos_token_id\n",
                "            )\n",
                "        \n",
                "        elapsed = time.time() - start\n",
                "        mem_after = get_gpu_memory()\n",
                "        \n",
                "        text = tokenizer.decode(output[0][input_ids.input_ids.shape[1]:], skip_special_tokens=True)\n",
                "        \n",
                "        del model\n",
                "        del tokenizer\n",
                "        torch.cuda.empty_cache()\n",
                "        torch.cuda.synchronize()\n",
                "        \n",
                "        times.append(elapsed)\n",
                "        memories.append(mem_after - mem_before)\n",
                "        texts.append(text)\n",
                "    \n",
                "    return {\n",
                "        'times': times,\n",
                "        'avg_time': np.mean(times),\n",
                "        'std_time': np.std(times),\n",
                "        'memories': memories,\n",
                "        'avg_memory': np.mean(memories),\n",
                "        'texts': texts,\n",
                "        'tokens_per_sec': max_tokens / np.mean(times)\n",
                "    }\n",
                "\n",
